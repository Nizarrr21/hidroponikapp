class PlantData {
  final int id;
  final String namaIndonesia;
  final String namaInggris;
  final String kategori;
  final double ecMin;
  final double ecMax;
  final int ppmMin;
  final int ppmMax;
  final String pupukSemai;
  final String pupukVegetatif;
  final String pupukGeneratif;
  final String keterangan;
  final int masaPanenMin;
  final int masaPanenMax;
  final String icon;
  final String color;

  PlantData({
    required this.id,
    required this.namaIndonesia,
    required this.namaInggris,
    required this.kategori,
    required this.ecMin,
    required this.ecMax,
    required this.ppmMin,
    required this.ppmMax,
    required this.pupukSemai,
    required this.pupukVegetatif,
    required this.pupukGeneratif,
    required this.keterangan,
    required this.masaPanenMin,
    required this.masaPanenMax,
    required this.icon,
    required this.color,
  });

  // Convert to/from JSON for storage
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'namaIndonesia': namaIndonesia,
      'namaInggris': namaInggris,
      'kategori': kategori,
      'ecMin': ecMin,
      'ecMax': ecMax,
      'ppmMin': ppmMin,
      'ppmMax': ppmMax,
      'pupukSemai': pupukSemai,
      'pupukVegetatif': pupukVegetatif,
      'pupukGeneratif': pupukGeneratif,
      'keterangan': keterangan,
      'masaPanenMin': masaPanenMin,
      'masaPanenMax': masaPanenMax,
      'icon': icon,
      'color': color,
    };
  }

  factory PlantData.fromJson(Map<String, dynamic> json) {
    return PlantData(
      id: json['id'],
      namaIndonesia: json['namaIndonesia'],
      namaInggris: json['namaInggris'],
      kategori: json['kategori'],
      ecMin: json['ecMin'].toDouble(),
      ecMax: json['ecMax'].toDouble(),
      ppmMin: json['ppmMin'],
      ppmMax: json['ppmMax'],
      pupukSemai: json['pupukSemai'],
      pupukVegetatif: json['pupukVegetatif'],
      pupukGeneratif: json['pupukGeneratif'],
      keterangan: json['keterangan'],
      masaPanenMin: json['masaPanenMin'],
      masaPanenMax: json['masaPanenMax'],
      icon: json['icon'],
      color: json['color'],
    );
  }

  String get masaPanen => '$masaPanenMin-$masaPanenMax hari';
  String get ppmRange => '$ppmMin-$ppmMax PPM';
  String get ecRange => '$ecMin-$ecMax EC';
}

