  static List<String> get categories {
    return plants.map((plant) => plant.kategori).toSet().toList();
  }

  static List<PlantData> getPlantsByCategory(String category) {
    return plants.where((plant) => plant.kategori == category).toList();
  }

  static PlantData? getPlantById(int id) {
    try {
      return plants.firstWhere((plant) => plant.id == id);
    } catch (e) {
      return null;
    }
  }

  static List<PlantData> searchPlants(String query) {
    return plants.where((plant) =>
      plant.namaIndonesia.toLowerCase().contains(query.toLowerCase()) ||
      plant.namaInggris.toLowerCase().contains(query.toLowerCase()) ||
      plant.kategori.toLowerCase().contains(query.toLowerCase())
    ).toList();
  }
